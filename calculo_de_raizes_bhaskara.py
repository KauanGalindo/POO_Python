import math

print(' ----------- Calculo de raizes Bhaskara -----------\n')

a = float(input('valor de a: '))
b = float(input('valor de b: '))
c = float(input('valor de c: '))

delta = b ** 2 - 4 * a * c

if (delta < 0):
  print('nÃ£o existe raiz!!!')

if (delta == 0):
  raizX1 = (-b + math.sqrt(delta)) / (2 * a)
  print('Existe apenas uma raiz!', raizX1)
else:
  if (delta > 0):
    raizX1 = (-b + math.sqrt(delta)) / (2 * a)
    raizX2 = (-b - math.sqrt(delta)) / (2 * a)
    print('raiz x1:',raizX1, 'raiz x2:', raizX2)